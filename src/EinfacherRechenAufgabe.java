public class EinfacherRechenAufgabe {

    public static void main(String[] args) {

        int num1 = 3;
        int num2 = 1;
        System.out.println(num1 + " + " + num2 + " = " + (num1 + num2));
        System.out.println(num1 + " - " + num2 + " = " + (num1 - num2));
        System.out.println(num1 + " * " + num2 + " = " + (num1 * num2));
        System.out.println(num1 + " / " + num2 + " = " + (num1 / num2));

        float num3 = 1.0f;
        float num4 = 3.0f;
        System.out.println(num3 + " + " + num4 + " = " + (num3 + num4));
        System.out.println(num3 + " - " + num4 + " = " + (num3 - num4));
        System.out.println(num3 + " * " + num4 + " = " + (num3 * num4));
        System.out.println(num3 + " / " + num4 + " = " + (num3 / num4));
    }


}
